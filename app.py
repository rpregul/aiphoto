import os
import io
import traceback
from google import genai
from google.genai import types
from telegram import Update, InlineKeyboardButton, InlineKeyboardMarkup
from telegram.ext import ApplicationBuilder, CommandHandler, MessageHandler, CallbackQueryHandler, filters, ContextTypes

# --- –ö–û–ù–§–ò–ì–£–†–ê–¶–ò–Ø ---
TELEGRAM_TOKEN = os.getenv("TELEGRAM_TOKEN")
GOOGLE_API_KEY = os.getenv("GOOGLE_API_KEY")

client = genai.Client(api_key=GOOGLE_API_KEY)
user_sessions = {}

async def start(update: Update, context: ContextTypes.DEFAULT_TYPE):
    await update.message.reply_text("üëã –ü—Ä–∏–≤–µ—Ç! –ü—Ä–∏—à–ª–∏ —Ñ–æ—Ç–æ –æ–¥–µ–∂–¥—ã, –∏ —è —Å–¥–µ–ª–∞—é –ø—Ä–∏–º–µ—Ä–∫—É.")

async def handle_photo(update: Update, context: ContextTypes.DEFAULT_TYPE):
    try:
        photo = update.message.photo[-1]
        file = await photo.get_file()
        file_path = f"garment_{update.effective_chat.id}.jpg"
        await file.download_to_drive(file_path)
        user_sessions[update.effective_chat.id] = file_path
        
        keyboard = [[InlineKeyboardButton("–ñ–µ–Ω—Å–∫–∞—è –º–æ–¥–µ–ª—å", callback_data="female")],
                    [InlineKeyboardButton("–ú—É–∂—Å–∫–∞—è –º–æ–¥–µ–ª—å", callback_data="male")]]
        await update.message.reply_text("–í—ã–±–µ—Ä–∏ –ø–æ–ª:", reply_markup=InlineKeyboardMarkup(keyboard))
    except:
        await update.message.reply_text("–û—à–∏–±–∫–∞ –∑–∞–≥—Ä—É–∑–∫–∏ —Ñ–æ—Ç–æ.")

async def handle_choice(update: Update, context: ContextTypes.DEFAULT_TYPE):
    query = update.callback_query
    await query.answer()
    chat_id = query.message.chat.id
    await query.edit_message_text("‚è≥ –ì–µ–Ω–µ—Ä–∏—Ä—É—é –æ–±—Ä–∞–∑ —á–µ—Ä–µ–∑ Gemini 2.5 Image...")

    try:
        garment_path = user_sessions.get(chat_id)
        if not garment_path: return

        with open(garment_path, "rb") as f:
            image_bytes = f.read()

        gender = "female" if query.data == "female" else "male"
        prompt = f"A professional photo of a {gender} model wearing the exact clothing from this image. Realistic fashion photography."

        # –ò—Å–ø–æ–ª—å–∑—É–µ–º –í –¢–û–ß–ù–û–°–¢–ò —Ç—É –º–æ–¥–µ–ª—å –∏ –∫–æ–Ω—Ñ–∏–≥, —á—Ç–æ –¥–∞–ª–∞ –°—Ç—É–¥–∏—è
        response = client.models.generate_content(
            model="gemini-2.5-flash-image",
            contents=[
                types.Part.from_bytes(data=image_bytes, mime_type="image/jpeg"),
                types.Part.from_text(text=prompt)
            ],
            config=types.GenerateContentConfig(
                response_modalities=["IMAGE", "TEXT"]
            )
        )

        image_sent = False
        if response.candidates and response.candidates[0].content.parts:
            for part in response.candidates[0].content.parts:
                if part.inline_data:
                    img_io = io.BytesIO(part.inline_data.data)
                    img_io.name = 'result.png'
                    await context.bot.send_photo(chat_id=chat_id, photo=img_io, caption="–ì–æ—Ç–æ–≤–æ! ‚ú®")
                    image_sent = True
                    break
        
        if not image_sent:
            await context.bot.send_message(chat_id, "–ò–ò –Ω–µ —Å–≥–µ–Ω–µ—Ä–∏—Ä–æ–≤–∞–ª –∫–∞—Ä—Ç–∏–Ω–∫—É. –í–æ–∑–º–æ–∂–Ω–æ, —Å—Ä–∞–±–æ—Ç–∞–ª–∏ —Ñ–∏–ª—å—Ç—Ä—ã –±–µ–∑–æ–ø–∞—Å–Ω–æ—Å—Ç–∏ Google.")

    except Exception as e:
        err_msg = str(e)
        print(f"–ö—Ä–∏—Ç–∏—á–µ—Å–∫–∞—è –æ—à–∏–±–∫–∞:\n{traceback.format_exc()}")
        if "429" in err_msg:
            await context.bot.send_message(chat_id, "–û—à–∏–±–∫–∞: –ü—Ä–µ–≤—ã—à–µ–Ω –ª–∏–º–∏—Ç –∑–∞–ø—Ä–æ—Å–æ–≤ (Quota Exceeded). –ü–æ–¥–æ–∂–¥–∏—Ç–µ 1-2 –º–∏–Ω—É—Ç—ã.")
        else:
            await context.bot.send_message(chat_id, f"–û—à–∏–±–∫–∞ API: {err_msg[:50]}")
    finally:
        if chat_id in user_sessions and os.path.exists(user_sessions[chat_id]):
            os.remove(user_sessions[chat_id])

if __name__ == "__main__":
    app = ApplicationBuilder().token(TELEGRAM_TOKEN).build()
    app.add_handler(CommandHandler("start", start))
    app.add_handler(MessageHandler(filters.PHOTO, handle_photo))
    app.add_handler(CallbackQueryHandler(handle_choice))

    print("–ë–æ—Ç –∑–∞–ø—É—Å–∫–∞–µ—Ç—Å—è...")
    # –ò–≥–Ω–æ—Ä–∏—Ä—É–µ–º –Ω–∞–∫–æ–ø–ª–µ–Ω–Ω—ã–µ —Å–æ–æ–±—â–µ–Ω–∏—è, —á—Ç–æ–±—ã –Ω–µ —Å–ø–∞–º–∏—Ç—å –≤ —Å–ª—É—á–∞–µ Conflict
    app.run_polling(drop_pending_updates=True)
